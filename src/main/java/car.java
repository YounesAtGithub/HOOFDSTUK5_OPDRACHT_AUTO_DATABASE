public class car {
    String brand;
    String model;
    int year;
    String color;
    double engineSize;

    public car(String brand, String model, int year, String color, double engineSize) {
        this.brand = brand;
        this.model = model;
        this.year = year;
        this.color = color;
        this.engineSize = engineSize;
    }


}
